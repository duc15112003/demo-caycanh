CREATE TABLE public.users (
    userid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fullname character varying(100) NOT NULL,
    email character varying(150) NOT NULL UNIQUE,
    password character varying(255) NOT NULL,
    phone character varying(20),
    address character varying(255),
    role character varying(20) NOT NULL DEFAULT 'USER',
    enabled boolean DEFAULT TRUE,
    createdat timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT users_role_check CHECK (role IN ('ADMIN', 'USER'))
);

CREATE TABLE public.categories (
    categoryid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    categoryname character varying(100) NOT NULL,
    description text,
    icon character varying(100) DEFAULT 'plant-icon.png'
);

CREATE TABLE public.products (
    productid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    categoryid integer NOT NULL,
    productname character varying(150) NOT NULL,
    description text,
    careguide text,
    price numeric(18,2) NOT NULL,
    stock integer DEFAULT 0,
    image character varying(255),
    isactive boolean DEFAULT TRUE,
    createdat timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT products_categoryid_fkey FOREIGN KEY (categoryid)
        REFERENCES public.categories(categoryid)
);

CREATE TABLE public.orders (
    orderid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    userid integer NOT NULL,
    orderdate timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    status character varying(50) DEFAULT 'Chờ xử lý',
    totalamount numeric(18,2) DEFAULT 0,
    shippingaddress character varying(255) NOT NULL,
    note text,
    CONSTRAINT orders_userid_fkey FOREIGN KEY (userid)
        REFERENCES public.users(userid)
);

CREATE TABLE public.cart (
    cartid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    userid integer NOT NULL,
    updatedat timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cart_userid_fkey FOREIGN KEY (userid)
        REFERENCES public.users(userid) ON DELETE CASCADE
);

CREATE TABLE public.orderitems (
    orderitemid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    orderid integer NOT NULL,
    productid integer NOT NULL,
    quantity integer NOT NULL,
    price numeric(18,2) NOT NULL,
    CONSTRAINT orderitems_orderid_fkey FOREIGN KEY (orderid)
        REFERENCES public.orders(orderid) ON DELETE CASCADE,
    CONSTRAINT orderitems_productid_fkey FOREIGN KEY (productid)
        REFERENCES public.products(productid)
);

CREATE TABLE public.payments (
    paymentid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    orderid integer NOT NULL,
    paymentmethod character varying(50) DEFAULT 'Tiền mặt',
    amount numeric(18,2) NOT NULL,
    paymentdate timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    issuccessful boolean DEFAULT FALSE,
    CONSTRAINT payments_paymentmethod_check CHECK (paymentmethod IN ('Tiền mặt', 'Chuyển khoản', 'Momo', 'Thẻ tín dụng')),
    CONSTRAINT payments_orderid_fkey FOREIGN KEY (orderid)
        REFERENCES public.orders(orderid) ON DELETE CASCADE
);

CREATE TABLE public.cartitems (
    cartitemid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cartid integer NOT NULL,
    productid integer NOT NULL,
    quantity integer NOT NULL DEFAULT 1,
    addedat timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cartitems_cartid_fkey FOREIGN KEY (cartid)
        REFERENCES public.cart(cartid) ON DELETE CASCADE,
    CONSTRAINT cartitems_productid_fkey FOREIGN KEY (productid)
        REFERENCES public.products(productid)
);

CREATE TABLE public.wishlist (
    wishlistid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    userid integer NOT NULL,
    productid integer NOT NULL,
    addedat timestamp without time zone DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT wishlist_userid_productid_key UNIQUE (userid, productid),
    CONSTRAINT wishlist_productid_fkey FOREIGN KEY (productid)
        REFERENCES public.products(productid) ON DELETE CASCADE,
    CONSTRAINT wishlist_userid_fkey FOREIGN KEY (userid)
        REFERENCES public.users(userid) ON DELETE CASCADE
);

CREATE OR REPLACE FUNCTION public.sp_registeruser(
    p_fullname character varying,
    p_email character varying,
    p_password character varying,
    p_phone character varying DEFAULT NULL,
    p_address character varying DEFAULT NULL,
    p_role character varying DEFAULT 'USER'
)
RETURNS TABLE(status integer, message character varying)
LANGUAGE plpgsql
AS $$
DECLARE
    v_newuserid integer;
BEGIN
    IF EXISTS (
        SELECT 1
        FROM public.users
        WHERE email = p_email
    ) THEN
        RETURN QUERY SELECT -1, 'Email đã tồn tại'::character varying;
        RETURN;
    END IF;

    INSERT INTO public.users (fullname, email, password, phone, address, role)
    VALUES (p_fullname, p_email, p_password, p_phone, p_address, p_role)
    RETURNING userid INTO v_newuserid;

    INSERT INTO public.cart (userid) VALUES (v_newuserid);

    RETURN QUERY SELECT 1, 'Đăng ký thành công'::character varying;
END;
$$;
